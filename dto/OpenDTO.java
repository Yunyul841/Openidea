package dto;

public class OpenDTO {
	String name = null;
	String title = null;
	String ion = null;
	
	
	
	public String getTitle() {
		return title;
	}
	public void setTitle(String title) {
		this.title = title;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name; 
	}
	public String getIon() {
		return ion;
	}
	public void setIon(String ion) {
		this.ion = ion;
	}
	
}
